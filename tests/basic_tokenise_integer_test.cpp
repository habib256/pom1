// basic_tokenise_integer_test.cpp -- byte-exact unit pin for the Apple-1 Integer
// BASIC (Woz) tokeniser + image layout (src/BasicTokeniserInteger.cpp). Pure: links
// only BasicTokeniserInteger.cpp (no emulator core, no ROM), so it always runs and is
// fast.
//
// Ground truth is the real $E000 ROM, dumped by the DEV ORACLE
// (tests/integer_oracle_dump.cpp -> build/tests/test_integer_oracle). The expected
// bytes below are that oracle's output. The image is the program area [pp, HIMEM)
// stored DOWNWARD from HIMEM ($1000): lines ascending by number, each line
// [length][num lo][num hi][tokens...][$01] where length = #tokens + 4.

#include "BasicTokeniserInteger.h"

#include <cstdint>
#include <cstdio>
#include <string>
#include <vector>

namespace {

int failures = 0;

void expectBytes(const char* what, const std::string& src,
                 const std::vector<uint8_t>& want)
{
    ibasic::Result r = ibasic::compile(src);
    if (!r.ok) {
        ++failures;
        std::printf("FAIL: %s\n  compile error: %s\n", what, r.error.c_str());
        return;
    }
    if (r.image == want) { std::printf("ok: %s\n", what); return; }
    ++failures;
    std::printf("FAIL: %s\n  got :", what);
    for (uint8_t b : r.image) std::printf(" %02X", b);
    std::printf("\n  want:");
    for (uint8_t b : want)    std::printf(" %02X", b);
    std::printf("\n");
}

} // namespace

int main()
{
    // Each case: source -> [pp, HIMEM) image bytes (exact ROM-oracle output).

    expectBytes("PRINT 42", "10 PRINT 42",
        {0x08,0x0A,0x00,0x62,0xB4,0x2A,0x00,0x01});

    expectBytes("PRINT 6*7", "10 PRINT 6*7",
        {0x0C,0x0A,0x00,0x62,0xB6,0x06,0x00,0x14,0xB7,0x07,0x00,0x01});

    expectBytes("LET A=5 / PRINT A", "10 LET A=5\n20 PRINT A",
        {0x0A,0x0A,0x00,0x5E,0xC1,0x71,0xB5,0x05,0x00,0x01,
         0x06,0x14,0x00,0x62,0xC1,0x01});

    expectBytes("FOR/NEXT loop", "10 FOR I=1 TO 10\n20 PRINT I\n30 NEXT I",
        {0x0E,0x0A,0x00,0x55,0xC9,0x56,0xB1,0x01,0x00,0x57,0xB1,0x0A,0x00,0x01,
         0x06,0x14,0x00,0x62,0xC9,0x01,
         0x06,0x1E,0x00,0x59,0xC9,0x01});

    expectBytes("IF A#3 THEN 100", "10 IF A#3 THEN 100",
        {0x0E,0x0A,0x00,0x60,0xC1,0x17,0xB3,0x03,0x00,0x24,0xB1,0x64,0x00,0x01});

    expectBytes("PRINT \"HELLO\"", "10 PRINT \"HELLO\"",
        {0x0C,0x0A,0x00,0x61,0x28,0xC8,0xC5,0xCC,0xCC,0xCF,0x29,0x01});

    expectBytes("implicit LET A=5", "10 A=5",
        {0x09,0x0A,0x00,0xC1,0x71,0xB5,0x05,0x00,0x01});

    expectBytes("GOTO 100", "10 GOTO 100",
        {0x08,0x0A,0x00,0x5F,0xB1,0x64,0x00,0x01});

    expectBytes("GOSUB 200 / RETURN", "10 GOSUB 200\n20 RETURN",
        {0x08,0x0A,0x00,0x5C,0xB2,0xC8,0x00,0x01,
         0x05,0x14,0x00,0x5B,0x01});

    expectBytes("END", "10 END",
        {0x05,0x0A,0x00,0x51,0x01});

    expectBytes("PRINT A;B", "20 PRINT A;B",
        {0x08,0x14,0x00,0x62,0xC1,0x46,0xC2,0x01});

    expectBytes("PRINT \"X=\";A", "30 PRINT \"X=\";A",
        {0x0B,0x1E,0x00,0x61,0x28,0xD8,0xBD,0x29,0x46,0xC1,0x01});

    expectBytes("A=B+C-D", "10 A=B+C-D",
        {0x0B,0x0A,0x00,0xC1,0x71,0xC2,0x12,0xC3,0x13,0xC4,0x01});

    expectBytes("IF A<3 THEN 100", "10 IF A<3 THEN 100",
        {0x0E,0x0A,0x00,0x60,0xC1,0x1C,0xB3,0x03,0x00,0x24,0xB1,0x64,0x00,0x01});

    expectBytes("IF A>3 THEN 100", "10 IF A>3 THEN 100",
        {0x0E,0x0A,0x00,0x60,0xC1,0x19,0xB3,0x03,0x00,0x24,0xB1,0x64,0x00,0x01});

    expectBytes("A=A+1: PRINT A", "10 A=A+1: PRINT A",
        {0x0E,0x0A,0x00,0xC1,0x71,0xC1,0x12,0xB1,0x01,0x00,0x03,0x62,0xC1,0x01});

    expectBytes("INPUT A", "10 INPUT A",
        {0x06,0x0A,0x00,0x54,0xC1,0x01});

    expectBytes("DIM A(5)", "10 DIM A(5)",
        {0x0B,0x0A,0x00,0x4F,0xC1,0x34,0xB5,0x05,0x00,0x72,0x01});

    expectBytes("PRINT A(3)", "10 PRINT A(3)",
        {0x0B,0x0A,0x00,0x62,0xC1,0x2D,0xB3,0x03,0x00,0x72,0x01});

    expectBytes("REM HELLO", "10 REM HELLO",
        {0x0B,0x0A,0x00,0x5D,0xA0,0xC8,0xC5,0xCC,0xCC,0xCF,0x01});

    // REM after a ':' is a statement — the rest of the physical line is a literal
    // comment (bytes built from the already-oracle-pinned A=1 / colon / REM
    // patterns above). Regression: the tokeniser used to reject this with
    // "expected '=' in assignment" because statement() had no REM case.
    // "A=1" -> C1 71 B1 01 00 ; ":" -> 03 ; "REM HI" -> 5D A0 C8 C9 ; EOL -> 01.
    expectBytes("A=1:REM HI (mid-line REM)", "10 A=1:REM HI",
        {0x0E,0x0A,0x00,0xC1,0x71,0xB1,0x01,0x00,0x03,0x5D,0xA0,0xC8,0xC9,0x01});

    expectBytes("POKE 0,1", "10 POKE 0,1",
        {0x0C,0x0A,0x00,0x64,0xB0,0x00,0x00,0x65,0xB1,0x01,0x00,0x01});

    expectBytes("A$=\"HI\"", "10 A$=\"HI\"",
        {0x0B,0x0A,0x00,0xC1,0x40,0x70,0x28,0xC8,0xC9,0x29,0x01});

    expectBytes("A=PEEK(0)", "10 A=PEEK(0)",
        {0x0C,0x0A,0x00,0xC1,0x71,0x2E,0x3F,0xB0,0x00,0x00,0x72,0x01});

    // PRINT separator tokens are chosen by the type of the item that FOLLOWS the
    // separator (oracle-verified): ";"+str=$45 ";"+num=$46 ";"end=$47
    // ","+str=$48 ","+num=$49.
    expectBytes("PRINT A,B", "10 PRINT A,B",
        {0x08,0x0A,0x00,0x62,0xC1,0x49,0xC2,0x01});
    expectBytes("PRINT \"A\",\"B\"", "10 PRINT \"A\",\"B\"",
        {0x0C,0x0A,0x00,0x61,0x28,0xC1,0x29,0x48,0x28,0xC2,0x29,0x01});
    expectBytes("PRINT \"A\";\"B\"", "10 PRINT \"A\";\"B\"",
        {0x0C,0x0A,0x00,0x61,0x28,0xC1,0x29,0x45,0x28,0xC2,0x29,0x01});
    expectBytes("PRINT A; (trailing)", "10 PRINT A;",
        {0x07,0x0A,0x00,0x62,0xC1,0x47,0x01});
    expectBytes("PRINT \"A\"; (trailing)", "10 PRINT \"A\";",
        {0x09,0x0A,0x00,0x61,0x28,0xC1,0x29,0x47,0x01});
    expectBytes("PRINT \"A\",B", "10 PRINT \"A\",B",
        {0x0A,0x0A,0x00,0x61,0x28,0xC1,0x29,0x49,0xC2,0x01});
    expectBytes("PRINT A;\"B\"", "10 PRINT A;\"B\"",
        {0x0A,0x0A,0x00,0x62,0xC1,0x45,0x28,0xC2,0x29,0x01});
    expectBytes("PRINT A,\"B\"", "10 PRINT A,\"B\"",
        {0x0A,0x0A,0x00,0x62,0xC1,0x48,0x28,0xC2,0x29,0x01});

    // ---- full-program constructs (oracle-verified bytes) -------------------
    // Relational / logical operators are ordinary numeric operators in the one
    // expression grammar:  = $16  # $17  >= $18  > $19  <= $1A  <> $1B  < $1C
    // AND $1D  OR $1E  MOD $1F.
    expectBytes("IF A>=3 THEN 100", "10 IF A>=3 THEN 100",
        {0x0E,0x0A,0x00,0x60,0xC1,0x18,0xB3,0x03,0x00,0x24,0xB1,0x64,0x00,0x01});
    expectBytes("IF A<=3 THEN 100", "10 IF A<=3 THEN 100",
        {0x0E,0x0A,0x00,0x60,0xC1,0x1A,0xB3,0x03,0x00,0x24,0xB1,0x64,0x00,0x01});
    expectBytes("IF A<>3 THEN 100", "10 IF A<>3 THEN 100",
        {0x0E,0x0A,0x00,0x60,0xC1,0x1B,0xB3,0x03,0x00,0x24,0xB1,0x64,0x00,0x01});
    expectBytes("IF D>17 OR D=17 THEN 80", "10 IF D>17 OR D=17 THEN 80",
        {0x14,0x0A,0x00,0x60,0xC4,0x19,0xB1,0x11,0x00,0x1E,0xC4,0x16,
         0xB1,0x11,0x00,0x24,0xB8,0x50,0x00,0x01});
    expectBytes("IF A2=0 AND P<22 THEN 80", "10 IF A2=0 AND P<22 THEN 80",
        {0x15,0x0A,0x00,0x60,0xC1,0xB2,0x16,0xB0,0x00,0x00,0x1D,0xD0,
         0x1C,0xB2,0x16,0x00,0x24,0xB8,0x50,0x00,0x01});
    expectBytes("K=A(N) MOD 4+1", "10 K=A(N) MOD 4+1",
        {0x12,0x0A,0x00,0xCB,0x71,0xC1,0x2D,0xCE,0x72,0x1F,0xB4,0x04,
         0x00,0x12,0xB1,0x01,0x00,0x01});

    // THEN before a LINE NUMBER is $24 (implied GOTO); before a STATEMENT it is $25.
    expectBytes("IF B THEN 35 (no relop)", "10 IF B THEN 35",
        {0x0A,0x0A,0x00,0x60,0xC2,0x24,0xB3,0x23,0x00,0x01});
    expectBytes("IF B<=3000 THEN RETURN", "10 IF B<=3000 THEN RETURN",
        {0x0C,0x0A,0x00,0x60,0xC2,0x1A,0xB3,0xB8,0x0B,0x25,0x5B,0x01});
    expectBytes("IF N>50 THEN GOSUB 1000", "10 IF N>50 THEN GOSUB 1000",
        {0x0F,0x0A,0x00,0x60,0xCE,0x19,0xB5,0x32,0x00,0x25,0x5C,0xB1,0xE8,0x03,0x01});

    // String comparison ($39 = / $3A #), and the chained OR keeps string context for
    // the second operand (this was the $16-vs-$39 bug fixed in the expression loop).
    expectBytes("IF D$=\"N\" OR D$=\"NO\" THEN 50", "10 IF D$=\"N\" OR D$=\"NO\" THEN 50",
        {0x17,0x0A,0x00,0x60,0xC4,0x40,0x39,0x28,0xCE,0x29,0x1E,0xC4,0x40,0x39,
         0x28,0xCE,0xCF,0x29,0x24,0xB5,0x32,0x00,0x01});
    expectBytes("IF D$#\"Y\" AND D$#\"YES\" THEN 44", "10 IF D$#\"Y\" AND D$#\"YES\" THEN 44",
        {0x18,0x0A,0x00,0x60,0xC4,0x40,0x3A,0x28,0xD9,0x29,0x1D,0xC4,0x40,0x3A,
         0x28,0xD9,0xC5,0xD3,0x29,0x24,0xB4,0x2C,0x00,0x01});

    // Grouping "(" $38, with a comparison inside producing a numeric value.
    expectBytes("D=D+C+(C=1)*10", "10 D=D+C+(C=1)*10",
        {0x15,0x0A,0x00,0xC4,0x71,0xC4,0x12,0xC3,0x12,0x38,0xC3,0x16,0xB1,0x01,
         0x00,0x72,0x14,0xB1,0x0A,0x00,0x01});
    expectBytes("A=(B>C)+(D<E)", "10 A=(B>C)+(D<E)",
        {0x11,0x0A,0x00,0xC1,0x71,0x38,0xC2,0x19,0xC3,0x72,0x12,0x38,0xC4,0x1C,
         0xC5,0x72,0x01});

    // Multi-var string DIM ($4E first, $43 next-string, $44 next-numeric), "(" $22/$34.
    expectBytes("DIM A$(13)", "10 DIM A$(13)",
        {0x0C,0x0A,0x00,0x4E,0xC1,0x40,0x22,0xB1,0x0D,0x00,0x72,0x01});
    expectBytes("DIM A$(13),B$(4)", "10 DIM A$(13),B$(4)",
        {0x14,0x0A,0x00,0x4E,0xC1,0x40,0x22,0xB1,0x0D,0x00,0x72,0x43,0xC2,0x40,
         0x22,0xB4,0x04,0x00,0x72,0x01});
    expectBytes("DIM A$(13),A(52)", "10 DIM A$(13),A(52)",
        {0x13,0x0A,0x00,0x4E,0xC1,0x40,0x22,0xB1,0x0D,0x00,0x72,0x44,0xC1,0x34,
         0xB5,0x34,0x00,0x72,0x01});

    // Substring element access A$(a,b): "(" $2A, "," $23, ")" $72.
    expectBytes("PRINT A$(E,E)", "10 PRINT A$(E,E)",
        {0x0C,0x0A,0x00,0x61,0xC1,0x40,0x2A,0xC5,0x23,0xC5,0x72,0x01});
    expectBytes("PRINT A$(J,J);B$(K,K)", "10 PRINT A$(J,J);B$(K,K)",
        {0x14,0x0A,0x00,0x61,0xC1,0x40,0x2A,0xCA,0x23,0xCA,0x72,0x45,0xC2,0x40,
         0x2A,0xCB,0x23,0xCB,0x72,0x01});

    // INPUT with a literal prompt: $53 prompt then comma ($26 string / $27 numeric var).
    expectBytes("INPUT \"BET\",B", "10 INPUT \"BET\",B",
        {0x0C,0x0A,0x00,0x53,0x28,0xC2,0xC5,0xD4,0x29,0x27,0xC2,0x01});
    expectBytes("INPUT \"NAME\",C$", "10 INPUT \"NAME\",C$",
        {0x0E,0x0A,0x00,0x53,0x28,0xCE,0xC1,0xCD,0xC5,0x29,0x26,0xC3,0x40,0x01});

    // TAB n  and  TAB (n) (grouping token $38 inside).
    expectBytes("TAB 11", "10 TAB 11",
        {0x08,0x0A,0x00,0x50,0xB1,0x0B,0x00,0x01});
    expectBytes("TAB (4): PRINT \"X\"", "10 TAB (4): PRINT \"X\"",
        {0x0F,0x0A,0x00,0x50,0x38,0xB4,0x04,0x00,0x72,0x03,0x61,0x28,0xD8,0x29,0x01});

    // String-array element as destination: "(" $42, then $70 string-LET.
    expectBytes("A$(1)=\"FOO\"", "10 A$(1)=\"FOO\"",
        {0x11,0x0A,0x00,0xC1,0x40,0x42,0xB1,0x01,0x00,0x72,0x70,0x28,0xC6,0xCF,
         0xCF,0x29,0x01});

    // RND with a leading space, GOTO of an expression.
    expectBytes("GOTO 210+ RND (5)", "10 GOTO 210+ RND (5)",
        {0x0F,0x0A,0x00,0x5F,0xB2,0xD2,0x00,0x12,0x2F,0x3F,0xB5,0x05,0x00,0x72,0x01});

    if (failures) { std::printf("basic_tokenise_integer: %d FAILED\n", failures); return 1; }
    std::printf("basic_tokenise_integer: OK\n");
    return 0;
}
