COLOURS = {"dog": WHITE, "octopus": BLUE, "bat": GREEN, "lion": ORANGE}
COLNAME = {WHITE: "WHITE", BLUE: "BLUE", GREEN: "GREEN", ORANGE: "ORANGE"}

WBYTES, H = 3, 16


def inflate_x2(mono, wbytes, h, color):
    dW, dH, wpx = wbytes * 2, h * 2, wbytes * 7
    litLeft  = color in (VIOLET, BLUE, WHITE)
    litRight = color in (GREEN, ORANGE, WHITE)
    p1       = color in (BLUE, ORANGE)
    out = [0] * (dW * dH)
    for sy in range(h):
        rowbase = (sy + sy) * dW
        for sx in range(wpx):
            if (mono[sy * wbytes + sx // 7] & (1 << (sx % 7))) == 0:
                continue
            for k in range(2):
                if (k == 0 and not litLeft) or (k == 1 and not litRight):
                    continue
                dc = (sx << 1) + k
                byte, bit = dc // 7, dc % 7
                out[rowbase + byte] |= (1 << bit)
                out[rowbase + dW + byte] |= (1 << bit)
                if p1:
                    out[rowbase + byte] |= 0x80
                    out[rowbase + dW + byte] |= 0x80
    return out


def emit(name):
    data = inflate_x2(MASTERS[name], WBYTES, H, COLOURS[name])
    dW = WBYTES * 2
    lines = [f"/* {name} -> x2 {COLNAME[COLOURS[name]]}: 6 bytes x 32 rows */"]
    lines.append(f"static const unsigned char x2_{name}[192] = {{")
    for r in range(0, len(data), dW):
        row = ",".join(f"0x{b:02X}" for b in data[r:r + dW])
        lines.append(f"    {row},")
    lines.append("};")
    return "\n".join(lines)


if __name__ == "__main__":
    print("/* AUTO-GENERATED by gen_x2.py — do not edit. */")
    for n in ("dog", "octopus", "bat", "lion"):
        print(emit(n))
        print()
