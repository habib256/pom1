/*
 * GEN2Animals.c — HGR equivalent of the TMS9918 demo_sprite_animals, on
 *                 Uncle Bernie's GEN2 Color Graphics Card (cc65 / gen2c).
 *
 *   GEN2 Fauna (8 HGR x2 colour sprites, 2px-smooth) / VERHILLE Arnaud 2026
 *
 * EIGHT SCROLL-O-SPRITES "Fauna" silhouettes as HGR x2 COLOUR sprites (one hue
 * each, source pixels doubled to span a full NTSC colour clock):
 *   dog=WHITE octopus=BLUE bat=GREEN lion=ORANGE
 *   rabbit=VIOLET spider=ORANGE cat=BLUE snake=GREEN
 *
 * SMOOTH 2px MOTION via 7 pre-shifted phases per animal (even shifts keep the
 * NTSC parity so the hue survives), baked OFFLINE by gen_x2.py. Runtime picks
 * phase (x%14)/2 at byte column 2*(x/14). Vertical is 1px-smooth.
 *
 * PERFORMANCE (per-frame budget is dominated by the 16 sprite blits):
 *   - INCREMENTAL: captions drawn once/page; each frame only touches sprite
 *     footprints. ERASE is a byte-column 0-FILL (gen2_hgr_fill_rect) of the
 *     footprint rectangle, NOT a CLEAR-blit of the shape: on a black field the
 *     fill's tight STA loop (no source read, no per-byte AND) is ~2x cheaper, and
 *     needs no bank/phase data. DRAW is a GEN2_SET blit (OR keeps overlaps).
 *   - BOUNDING-BOX TRIM, per-phase: each phase is baked to its OWN content box
 *     (yoff/rows + per-phase xoff/width, ~4-5 bytes not 7) — blit7 pays for zero
 *     bytes too. ~40% fewer byte-ops AND small enough (7.1 KB) to fit 8 sprites'
 *     banks even when the DevBench force-links the whole gen2c runtime.
 *   - ZERO per-frame mul/div: sine pre-scaled per amplitude + phase accumulators;
 *     x->(base,phase,bank offset) is a boot-built LUT (parked in LOWBSS).
 *   - Double-buffered (flip in V-blank -> no tear).
 *
 * Self-contained (the in-app DevBench compiles a single file); banks are baked,
 * so no runtime x2 inflate. ESC -> Wozmon; apple1_readkey is non-blocking.
 *
 *   Build : make   Run : POM1 --preset 11 --load 6000:GEN2Animals.bin --run 6000
 */
