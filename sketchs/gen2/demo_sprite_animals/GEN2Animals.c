/*
 * GEN2Animals.c — HGR equivalent of the TMS9918 demo_sprite_animals, on
 *                 Uncle Bernie's GEN2 Color Graphics Card (cc65 / gen2c).
 *
 *   GEN2 Fauna (HGR x2 colour sprites) / VERHILLE Arnaud 2026
 *
 * The TMS side (sketchs/tms9918/demo_sprite_animals) floats four SCROLL-O-SPRITES
 * "Fauna" silhouettes — dog, octopus, bat, lion — around the screen using the
 * TMS9918's HARDWARE sprites (one chosen colour per sprite, transparent
 * background). The Apple II has NO hardware sprites and HIRES has no per-pixel
 * colour, so this port reproduces the SAME four animals as HGR x2 COLOUR sprites:
 *
 *   x2 regime — each 16x16 mono master (dev/lib/gen2/sprites/sprites_fauna_hgr.asm,
 *   the auto-generated GEN2 mirror of the TMS fauna the C demo uses) is DOUBLED so
 *   every source pixel spans a full NTSC colour clock, lighting one dot + the
 *   palette bit so the whole sprite reads as ONE chosen hue instead of solid
 *   white. That transform is gen2_hgr_inflate_x2's; here it is done OFFLINE by
 *   gen_x2.py and the 6x32 (192 B) results are pasted in below as constants.
 *   Baking at build time keeps the demo off gen2_hgr_x2.o — so it also links
 *   under the in-app DevBench, whose GEN2-C set omits that module — and costs
 *   zero runtime cycles. Everything is in THIS ONE .c on purpose: the DevBench
 *   copies a single source to /tmp and compiles it, so a sibling .inc would not
 *   be found (rerun gen_x2.py and repaste the block if the masters change).
 *   (gen2_hgr_inflate_x2 is also miscompiled to all-zero by cc65 -Oirs on-target,
 *   so baking offline is the correct path regardless.)
 *
 *     dog = WHITE   octopus = BLUE   bat = GREEN   lion = ORANGE
 *
 * PARITY: an x2 sprite must land on an even HGR byte column (x = 14*n) or its hue
 * flips (Violet<->Green, Blue<->Orange). So horizontal motion moves in 14 px
 * steps; vertical is 1 px-smooth. Double-buffered (clear + redraw on the hidden
 * page, flip in V-blank) so the beam never catches a half-drawn frame.
 *
 * ESC -> Wozmon (repo convention); apple1_readkey is non-blocking so the bob
 * stays smooth.
 *
 *   Build : make    -> "software/Graphic HGR/GEN2Animals.bin" (+ .txt)
 *   Run   : build/POM1 --preset 11 \
 *               --load 6000:"software/Graphic HGR/GEN2Animals.bin" --run 6000
 */
