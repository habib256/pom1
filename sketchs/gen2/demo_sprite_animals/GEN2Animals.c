/* 8 animals in a rough 4x2 grid, each with its own Lissajous params. clampi
 * keeps them on-screen; overlaps are fine (erase+redraw). home/amp are 8-bit to
 * save RODATA (x<=224, y<=150, amp<=32). */
static const unsigned char homeX[NA] = {  36u,  96u, 156u, 206u,  36u,  96u, 156u, 206u };
static const unsigned char homeY[NA] = {  36u,  42u,  34u,  40u, 110u, 104u, 112u, 106u };
static const unsigned char pmX[NA]   = {   1u,   2u,   3u,   1u,   2u,   3u,   1u,   2u };
static const unsigned char offX[NA]  = {   0u,   0u,   0u,   8u,   4u,  12u,   6u,   2u };
static const signed char   ampX[NA]  = {  30,   28,   26,   16,   30,   28,   26,   16 };
static const unsigned char pmY[NA]   = {   1u,   1u,   3u,   5u,   2u,   1u,   3u,   2u };
static const unsigned char offY[NA]  = {   8u,  17u,  11u,   3u,   5u,  20u,  14u,  25u };
static const signed char   ampY[NA]  = {  22,   28,   20,   26,   30,   28,   32,   26 };

/* These runtime tables are UNINITIALISED (init_luts / set_pos fill them before
 * first read), so park them in LOWBSS ($0C00-$1FFF, idle below the framebuffers)
 * instead of the tight $6000-$BEFF BSS. That's what keeps 8 sprites fitting when
 * the in-app DevBench force-links the WHOLE gen2c runtime. crt0 does NOT zero
 * LOWBSS, which is fine: nothing here is read before it is written. See
 * [[gen2c_devbench_forcelink_lowbss]]. */
#define NX2 113u
#pragma bss-name(push, "LOWBSS")
static int cx[NA], cy[NA];
static signed char  sX[NA][32], sY[NA][32];   /* sine pre-scaled by amplitude    */
static unsigned char phX[NA], phY[NA];        /* phase accumulators              */
static unsigned char baseT[NX2];              /* base pixel   = (h/7)*14         */
static unsigned char baseCol[NX2];            /* base byte col = (h/7)*2 (erase) */
static unsigned char phTb[NX2];               /* phase = h%7 (0..6)              */
static unsigned char xoff7[NA][7];            /* per-phase content xoff * 7 (px) */
static unsigned      bankOff[NA][7];          /* per-phase bank byte offset (cum)*/
#pragma bss-name(pop)

static int clampi(int v, int lo, int hi) {
    if (v < lo) return lo;
    if (v > hi) return hi;
    return v;
}

static void init_luts(void) {
    unsigned char i, p, h, ph, nr;
    unsigned off;
    for (i = 0u; i < NA; ++i) {
        phX[i] = offX[i];
        phY[i] = offY[i];
        for (p = 0u; p < 32u; ++p) {
            sX[i][p] = (signed char)((int)sin32[p] * ampX[i] / 12);
            sY[i][p] = (signed char)((int)sin32[p] * ampY[i] / 12);
        }
        nr = sprRows[i];
        off = 0u;                          /* cumulative bank offset (phases vary) */
        for (ph = 0u; ph < 7u; ++ph) {
            xoff7[i][ph]   = (unsigned char)(sprXoff[i][ph] * 7u);
            bankOff[i][ph] = off;
            off += (unsigned)nr * sprWp[i][ph];
        }
    }
    for (h = 0u; h < NX2; ++h) {
        unsigned char q = (unsigned char)(h / 7u);
        baseT[h]   = (unsigned char)(q * 14u);   /* pixel of the 14-aligned base */
        baseCol[h] = (unsigned char)(q * 2u);    /* its byte column (= pixel/7)  */
        phTb[h]    = (unsigned char)(h % 7u);
    }
}

static void set_pos(void) {
    unsigned char i;
    for (i = 0u; i < NA; ++i) {
        cx[i] = clampi((int)homeX[i] + sX[i][phX[i] & 31u], 0, 224);
        cy[i] = clampi((int)homeY[i] + sY[i][phY[i] & 31u], 14, 150);
        phX[i] = (unsigned char)(phX[i] + pmX[i]);
        phY[i] = (unsigned char)(phY[i] + pmY[i]);
    }
}

/* DRAW: OR the sprite's trimmed sub-image (SET keeps overlaps additive). */
static void draw_animal(unsigned char id, int x, int y) {
    unsigned char h  = (unsigned char)((unsigned)x >> 1);
    unsigned char ph = phTb[h];
    gen2_hgr_blit7((unsigned)baseT[h] + xoff7[id][ph],
                   (unsigned char)(y + sprYoff[id]),
                   sprWp[id][ph], sprRows[id], spr[id] + bankOff[id][ph], GEN2_SET);
}

/* ERASE: on a black field, clearing the footprint is a byte-column 0-FILL, not
 * a CLEAR-blit of the shape. gen2_hgr_fill_rect's inner loop is a tight STA (no
 * source read, no per-byte AND/EOR) -> ~2x cheaper than GEN2_CLEAR, and the erase
 * needs no bank/phase data — just the same rectangle the draw covered. */
static void erase_animal(unsigned char id, int x, int y) {
    unsigned char h  = (unsigned char)((unsigned)x >> 1);
    unsigned char ph = phTb[h];
    gen2_hgr_fill_rect((unsigned char)(y + sprYoff[id]), sprRows[id],
                       (unsigned char)(baseCol[h] + sprXoff[id][ph]),
                       sprWp[id][ph], 0u);
}

void main(void) {
    unsigned char page = 1u, pidx, i, pg;
    int ox[NA][2], oy[NA][2];

    gen2_hgr_init();
    init_luts();

    for (pg = 1u; pg <= 2u; ++pg) {
        gen2_set_draw_page(pg);
        gen2_hgr_clear(0u);
        gen2_hgr_puts8(4u, 4u, "FAUNA - HGR X2 (8 SPRITES)");
        gen2_hgr_puts8(4u, 184u, "DOG OCT BAT LION RAB SPI CAT SNK  ESC");
    }

    set_pos();
    for (i = 0u; i < NA; ++i) { ox[i][0] = ox[i][1] = cx[i]; oy[i][0] = oy[i][1] = cy[i]; }

    for (;;) {
        pidx = (unsigned char)(page - 1u);
        gen2_set_draw_page(page);

        for (i = 0u; i < NA; ++i)                 /* erase old footprints (0-fill) */
            erase_animal(i, ox[i][pidx], oy[i][pidx]);
        for (i = 0u; i < NA; ++i) {               /* draw new positions            */
            draw_animal(i, cx[i], cy[i]);
            ox[i][pidx] = cx[i];
            oy[i][pidx] = cy[i];
        }

        gen2_wait_vbl();
        gen2_show_page();
        page = (page == 1u) ? 2u : 1u;

        set_pos();
        if (apple1_readkey() == 27u) woz_mon();
    }
}
