/*
 * GEN2Animals.c — HGR equivalent of the TMS9918 demo_sprite_animals, on
 *                 Uncle Bernie's GEN2 Color Graphics Card (cc65 / gen2c).
 *
 *   GEN2 Fauna (HGR x2 colour sprites, 2px-smooth) / VERHILLE Arnaud 2026
 *
 * The TMS side (sketchs/tms9918/demo_sprite_animals) floats four SCROLL-O-SPRITES
 * "Fauna" silhouettes — dog, octopus, bat, lion — with the TMS9918's HARDWARE
 * sprites. The Apple II has none and HIRES has no per-pixel colour, so this port
 * reproduces the same four animals as HGR x2 COLOUR sprites (one chosen hue each,
 * each source pixel doubled to span a full NTSC colour clock):
 *
 *     dog = WHITE   octopus = BLUE   bat = GREEN   lion = ORANGE
 *
 * SMOOTH 2px MOTION. A byte-aligned blit places an x2 sprite only every 14px or
 * the hue flips. To move in 2px steps we BAKE each animal at the 7 even sub-
 * positions of a 14px period (shifts 0,2,4,6,8,10,12px) — an even shift keeps the
 * NTSC parity so the hue survives; the palette bit is re-derived per shifted byte.
 * Each phase is 8 bytes x 32 rows (256 B); 7 phases = 1792 B/animal, baked OFFLINE
 * by gen_x2.py and pasted below (this ONE .c stays self-contained so the in-app
 * DevBench — which compiles a single file — can build it; the DevBench GEN2-C set
 * also omits gen2_hgr_x2.o, so no runtime inflate). At runtime: for pixel x, blit
 * phase (x%14)/2 at byte column 2*(x/14). Vertical is 1px-smooth.
 *
 * Double-buffered (clear + redraw on the hidden page, flip in V-blank -> no tear).
 * ESC -> Wozmon; apple1_readkey is non-blocking so the bob stays smooth.
 *
 *   Build : make    -> "software/Graphic HGR/GEN2Animals.bin" (+ .txt)
 *   Run   : build/POM1 --preset 11 \
 *               --load 6000:"software/Graphic HGR/GEN2Animals.bin" --run 6000
 */
