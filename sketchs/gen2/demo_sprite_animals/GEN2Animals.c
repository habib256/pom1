/*
 * GEN2Animals.c — HGR equivalent of the TMS9918 demo_sprite_animals, on
 *                 Uncle Bernie's GEN2 Color Graphics Card (cc65 / gen2c).
 *
 *   GEN2 Fauna (HGR x2 colour sprites, 2px-smooth) / VERHILLE Arnaud 2026
 *
 * Four SCROLL-O-SPRITES "Fauna" silhouettes — dog, octopus, bat, lion — floated
 * around as HGR x2 COLOUR sprites (one chosen hue each, source pixels doubled to
 * span a full NTSC colour clock):
 *
 *     dog = WHITE   octopus = BLUE   bat = GREEN   lion = ORANGE
 *
 * SMOOTH 2px MOTION. A byte-aligned blit places an x2 sprite only every 14px or
 * the hue flips. So each animal is BAKED at the 7 even sub-positions of a 14px
 * period (shifts 0,2,4,6,8,10,12px) — an even shift keeps NTSC parity so the hue
 * survives; the palette bit is re-derived per shifted byte. A 16px master (x2 =
 * 32px) shifted up to +12 reaches col 43, so each phase fits 7 bytes x 32 rows
 * (224 B); 7 phases = 1568 B/animal, baked OFFLINE by gen_x2.py and pasted below.
 * At runtime: blit phase (x%14)/2 at byte column 2*(x/14). Vertical is 1px-smooth.
 *
 * FAST INCREMENTAL RENDER. A full clear + text redraw every frame ran ~15 FPS.
 * Instead the two captions are drawn ONCE per page (they never move) and each
 * frame only ERASES the 4 old sprite footprints (a byte-exact GEN2_CLEAR of the
 * same bytes, over the black play field) and redraws at the new spots — no 8KB
 * clear, no text re-render. Double-buffered (flip in V-blank -> no tear); as in
 * GEN2Bounces each page keeps its own 1-step-old positions (ox/oy[.][page]).
 *
 * This ONE .c stays self-contained so the in-app DevBench (single-file compile)
 * can build it; the pre-shift banks are baked, so no runtime x2 inflate.
 * ESC -> Wozmon; apple1_readkey is non-blocking.
 *
 *   Build : make    -> "software/Graphic HGR/GEN2Animals.bin" (+ .txt)
 *   Run   : build/POM1 --preset 11 \
 *               --load 6000:"software/Graphic HGR/GEN2Animals.bin" --run 6000
 */
