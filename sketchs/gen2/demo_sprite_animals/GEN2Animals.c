static const unsigned char *const spr[NA] = { x2ps_dog, x2ps_octopus, x2ps_bat, x2ps_lion };

/* sin(2*pi*i/32)*12, same table as the TMS demo. */
static const signed char sin32[32] = {
    0, 2, 5, 7, 8, 10, 11, 12, 12, 12, 11, 10, 8, 7, 5, 2,
    0, -2, -5, -7, -8, -10, -11, -12, -12, -12, -11, -10, -8, -7, -5, -2
};

/* Per-animal motion: home, sine phase multiplier + offset and amplitude for
 * x/y. x is now 2px-smooth (any even pixel), y is 1px-smooth. */
static const int           homeX[NA] = {  40, 150,  70, 150 };
static const int           homeY[NA] = {  36,  40, 108, 104 };
static const unsigned char pmX[NA]   = {   1u,  2u,  3u,  1u };
static const unsigned char offX[NA]  = {   0u,  0u,  0u,  8u };
static const int           ampX[NA]  = {  36,  30,  40,  44 };
static const unsigned char pmY[NA]   = {   1u,  1u,  3u,  5u };
static const unsigned char offY[NA]  = {   8u, 17u, 11u,  3u };
static const int           ampY[NA]  = {  40,  32,  40,  40 };

static int clampi(int v, int lo, int hi) {
    if (v < lo) return lo;
    if (v > hi) return hi;
    return v;
}

/* Blit an animal's baked x2 sprite at pixel (x,y), x forced even. Selects the
 * pre-shift phase for x%14 and the byte column for x/14. */
static void draw_animal(unsigned char id, int x, int y) {
    unsigned ux, base;
    unsigned char ph;
    ux   = (unsigned)x & ~1u;                 /* even pixel -> hue stable       */
    base = (ux / 14u) * 14u;                  /* byte-pair aligned origin       */
    ph   = (unsigned char)((ux - base) / 2u); /* 0..6 sub-position              */
    gen2_hgr_blit7(base, (unsigned char)y, PSW, PSH,
                   spr[id] + (unsigned)ph * PSB, GEN2_SET);
}

void main(void) {
    unsigned char page = 2u, i;
    unsigned int  t = 0u;
    int  px, py;
    signed char   sx, sy;

    gen2_hgr_init();                  /* graphics + hires + full + page 1 shown */

    for (;;) {
        gen2_set_draw_page(page);     /* draw into the HIDDEN page              */
        gen2_hgr_clear(0u);

        gen2_hgr_puts8(4u, 4u, "FAUNA - HGR X2 SPRITES (2PX SMOOTH)");
        gen2_hgr_puts8(4u, 184u, "DOG  OCTOPUS  BAT  LION   ESC=QUIT");

        for (i = 0u; i < NA; ++i) {
            sx = sin32[(unsigned char)(t * pmX[i] + offX[i]) & 31u];
            sy = sin32[(unsigned char)(t * pmY[i] + offY[i]) & 31u];
            px = homeX[i] + (int)sx * ampX[i] / 12;
            py = homeY[i] + (int)sy * ampY[i] / 12;
            px = clampi(px, 0, 224);
            py = clampi(py, 14, 158);
            draw_animal(i, px, py);
        }

        gen2_wait_vbl();              /* flip in blanking -> no tear            */
        gen2_show_page();
        page = (page == 1u) ? 2u : 1u;
        ++t;

        if (apple1_readkey() == 27u) {   /* ESC */
            woz_mon();
        }
    }
}
