static const unsigned char *const spr[NA] = { x2ps_dog, x2ps_octopus, x2ps_bat, x2ps_lion };

/* sin(2*pi*i/32)*12, same table as the TMS demo. */
static const signed char sin32[32] = {
    0, 2, 5, 7, 8, 10, 11, 12, 12, 12, 11, 10, 8, 7, 5, 2,
    0, -2, -5, -7, -8, -10, -11, -12, -12, -12, -11, -10, -8, -7, -5, -2
};

/* Per-animal motion: home + sine phase multiplier/offset/amplitude for x and y.
 * x is 2px-smooth (any even pixel), y is 1px-smooth. */
static const int           homeX[NA] = {  40, 150,  70, 150 };
static const int           homeY[NA] = {  36,  40, 108, 104 };
static const unsigned char pmX[NA]   = {   1u,  2u,  3u,  1u };
static const unsigned char offX[NA]  = {   0u,  0u,  0u,  8u };
static const int           ampX[NA]  = {  36,  30,  40,  44 };
static const unsigned char pmY[NA]   = {   1u,  1u,  3u,  5u };
static const unsigned char offY[NA]  = {   8u, 17u, 11u,  3u };
static const int           ampY[NA]  = {  40,  32,  40,  40 };

static int cx[NA], cy[NA];            /* current positions (this frame)         */

static int clampi(int v, int lo, int hi) {
    if (v < lo) return lo;
    if (v > hi) return hi;
    return v;
}

static void set_pos(unsigned int t) {
    unsigned char i;
    signed char sx, sy;
    for (i = 0u; i < NA; ++i) {
        sx = sin32[(unsigned char)(t * pmX[i] + offX[i]) & 31u];
        sy = sin32[(unsigned char)(t * pmY[i] + offY[i]) & 31u];
        cx[i] = clampi(homeX[i] + (int)sx * ampX[i] / 12, 0, 224);
        cy[i] = clampi(homeY[i] + (int)sy * ampY[i] / 12, 14, 150);
    }
}

/* Blit animal `id` at pixel (x,y) (x forced even): selects the pre-shift phase
 * for x%14 and the byte column for x/14. GEN2_SET draws, GEN2_CLEAR erases. */
static void blit_animal(unsigned char id, int x, int y, unsigned char mode) {
    unsigned ux   = (unsigned)x & ~1u;
    unsigned base = (ux / 14u) * 14u;
    unsigned char ph = (unsigned char)((ux - base) / 2u);
    gen2_hgr_blit7(base, (unsigned char)y, PSW, PSH,
                   spr[id] + (unsigned)ph * PSB, mode);
}

void main(void) {
    unsigned char page = 1u, pidx, i, pg;
    unsigned int  t = 0u;
    int ox[NA][2], oy[NA][2];         /* per-page 1-step-old positions          */

    gen2_hgr_init();

    /* Static captions, drawn ONCE on each page (the play field below stays black
     * and is the only thing the frame loop touches). */
    for (pg = 1u; pg <= 2u; ++pg) {
        gen2_set_draw_page(pg);
        gen2_hgr_clear(0u);
        gen2_hgr_puts8(4u, 4u, "FAUNA - HGR X2 SPRITES (2PX)");
        gen2_hgr_puts8(4u, 184u, "DOG  OCTOPUS  BAT  LION   ESC=QUIT");
    }

    set_pos(0u);
    for (i = 0u; i < NA; ++i) { ox[i][0] = ox[i][1] = cx[i]; oy[i][0] = oy[i][1] = cy[i]; }

    for (;;) {
        pidx = (unsigned char)(page - 1u);
        gen2_set_draw_page(page);

        /* erase this page's 4 old footprints, then draw the 4 new ones */
        for (i = 0u; i < NA; ++i)
            blit_animal(i, ox[i][pidx], oy[i][pidx], GEN2_CLEAR);
        for (i = 0u; i < NA; ++i) {
            blit_animal(i, cx[i], cy[i], GEN2_SET);
            ox[i][pidx] = cx[i];
            oy[i][pidx] = cy[i];
        }

        gen2_wait_vbl();
        gen2_show_page();
        page = (page == 1u) ? 2u : 1u;

        ++t;
        set_pos(t);

        if (apple1_readkey() == 27u) woz_mon();   /* ESC */
    }
}
